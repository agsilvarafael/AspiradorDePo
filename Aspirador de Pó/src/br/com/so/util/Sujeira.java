package br.com.so.util;

public class Sujeira {

	private Integer posX, posY;

	public Sujeira(Integer posX, Integer posY) {
		this.posX = posX;
		this.posY = posY;
	}

	public Integer getPosY() {
		return posY;
	}

	public void setPosY(Integer posY) {
		this.posY = posY;
	}
	
	
	
}
